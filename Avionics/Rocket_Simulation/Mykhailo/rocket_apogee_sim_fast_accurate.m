%% INITIAL STATE AT CUTOFF

total_velocity = vertical_velocity / cos(tilt_angle);

x = 0;
y = altitude;
vx = total_velocity * sin(tilt_angle);
vy = vertical_velocity;
t = 0;

%% LOG ONLY WHAT IS NEEDED FOR ONE GRAPH

max_steps = 30000;
time_log = zeros(max_steps, 1);
altitude_log = zeros(max_steps, 1);

k = 1;
time_log(k) = t;
altitude_log(k) = y;

%% MAIN SIMULATION LOOP

while vy > 0 && k < max_steps

    y_previous = y;
    vy_previous = vy;
    t_previous = t;

    speed = sqrt(vx*vx + vy*vy);

    % ---- Cd manual linear interpolation on 1 m/s table ----
    if speed <= 0
        Cd = Cd_from_V(1);
    elseif speed >= V_max_for_table
        Cd = Cd_from_V(V_max_for_table + 1);
    else
        q = speed * V_step_inv;
        i = floor(q) + 1;
        f = q - floor(q);
        Cd = Cd_from_V(i) * (1 - f) + Cd_from_V(i + 1) * f;
    end

    % ---- density manual linear interpolation on 50 m table ----
    if y <= 0
        rho = rho_from_altitude(1);
    elseif y >= rho_max_altitude_for_table
        rho = rho_from_altitude(rho_max_altitude_for_table / rho_altitude_step + 1);
    else
        q = y * rho_altitude_step_inv;
        i = floor(q) + 1;
        f = q - floor(q);
        rho = rho_from_altitude(i) * (1 - f) + rho_from_altitude(i + 1) * f;
    end

    % Drag magnitude.
    Fd = 0.5 * rho * Cd * cross_sectional_area * speed * speed;

    % Drag direction is opposite the velocity vector.
    ax = -(Fd * vx / speed) / rocket_mass_at_cut_off;
    ay = -(Fd * vy / speed) / rocket_mass_at_cut_off - g;

    % Semi-implicit Euler update.
    vx = vx + ax * dt;
    vy = vy + ay * dt;
    x = x + vx * dt;
    y = y + vy * dt;
    t = t + dt;

    k = k + 1;
    time_log(k) = t;
    altitude_log(k) = y;
end

%% TRIM LOGS

time_log = time_log(1:k);
altitude_log = altitude_log(1:k);

%% FINAL APOGEE
% Interpolate between the last positive-vy point and first negative-vy point.

if vy <= 0
    f_apogee = vy_previous / (vy_previous - vy);
    apogee_time = t_previous + f_apogee * (t - t_previous);
    apogee_m = y_previous + f_apogee * (y - y_previous);
else
    apogee_time = t;
    apogee_m = y;
end

apogee_ft = apogee_m / 0.3048;

fprintf("Final apogee = %.2f m = %.2f ft\n", apogee_m, apogee_ft);

